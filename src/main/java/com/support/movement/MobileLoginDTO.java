package com.support.movement;

public class MobileLoginDTO {
	private String driver_id;
	private String driver_pwd;
	private String driver_no;
	
	public String getDriver_id() {
		return driver_id;
	}
	public void setDriver_id(String driver_id) {
		this.driver_id = driver_id;
	}
	public String getDriver_pwd() {
		return driver_pwd;
	}
	public void setDriver_pwd(String driver_pwd) {
		this.driver_pwd = driver_pwd;
	}
	public String getDriver_no() {
		return driver_no;
	}
	public void setDriver_no(String driver_no) {
		this.driver_no = driver_no;
	}
	
}
